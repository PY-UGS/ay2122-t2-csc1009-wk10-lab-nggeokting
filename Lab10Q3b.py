# program to print odd number in descending order starting from 102 and ending with 66

# for loop from 102 to 66 in descending order
for i in range(102, 66, -1):
    # if i is not divisible by 2
    if (i % 2 == 1):
        # print the value of i
        print("value of x : {i}".format(i=i)) 
    